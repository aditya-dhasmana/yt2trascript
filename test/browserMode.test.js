const playerResponse = {
  videoDetails: {
    videoId: "M7lc1UVf-VE",
    title: "YouTube API demo",
    author: "Google for Developers",
  },
  captions: {
    playerCaptionsTracklistRenderer: {
      captionTracks: [
        {
          baseUrl: "https://www.youtube.com/api/timedtext?lang=es",
          languageCode: "es",
          name: { simpleText: "Spanish" },
        },
        {
          baseUrl: "https://www.youtube.com/api/timedtext?lang=en&kind=asr",
          languageCode: "en",
          kind: "asr",
          name: { simpleText: "English auto-generated" },
        },
        {
          baseUrl: "https://www.youtube.com/api/timedtext?lang=en",
          languageCode: "en",
          name: { simpleText: "English" },
        },
      ],
    },
  },
};

test("bookmarklet finds caption tracks and prefers manual English", () => {
  const script = `ytInitialPlayerResponse = ${JSON.stringify(playerResponse)};`;
  const extracted = extractPlayerResponseFromScripts([script], "M7lc1UVf-VE");
  const selected = selectPreferredTrack(normalizeCaptionTracks(extracted));

  assert.equal(selected.languageCode, "en");
  assert.equal(selected.isAutoGenerated, false);
  assert.equal(selected.name, "English");
});

test("bookmarklet parser supports JSON3, legacy XML, and srv3 XML", () => {
  const json = parseCaptionPayload(JSON.stringify({
    events: [{ tStartMs: 1000, dDurationMs: 1500, segs: [{ utf8: "JSON caption" }] }],
  }));
  const legacyXml = parseCaptionPayload(
    '<transcript><text start="2" dur="1">XML &amp; caption</text></transcript>',
  );
  const srv3 = parseCaptionPayload(
    '<timedtext><body><p t="3000" d="2000"><s>srv3 caption</s></p></body></timedtext>',
  );

  assert.deepEqual(json, [{ start: 1, duration: 1.5, text: "JSON caption" }]);
  assert.deepEqual(legacyXml, [{ start: 2, duration: 1, text: "XML & caption" }]);
  assert.deepEqual(srv3, [{ start: 3, duration: 2, text: "srv3 caption" }]);
});

test("receiver accepts only shaped payloads from a YouTube origin", () => {
  const payload = {
    version: 1,
    video: {
      videoId: "M7lc1UVf-VE",
      title: "YouTube API demo",
      channel: "Google for Developers",
      url: "https://www.youtube.com/watch?v=M7lc1UVf-VE",
    },
    track: { name: "English", languageCode: "en", isAutoGenerated: false },
    segments: [{ start: 1, duration: 2, text: "Receiver caption" }],
    importedAt: 1234,
  };

  assert.equal(validateBrowserModePayload(payload, "https://www.youtube.com").ok, true);
  assert.equal(validateBrowserModePayload(payload, "https://attacker.example").ok, false);
  assert.equal(validateBrowserModePayload({ ...payload, segments: [] }, "https://www.youtube.com").ok, false);
  assert.equal(validateBrowserModePayload({ videoUrl: payload.video.url }, "https://www.youtube.com").ok, false);
});

test("Browser Mode bookmarklet contains no backend transcript extraction route", () => {
  const bookmarklet = createBrowserModeBookmarklet(
    "https://www.yt2trascript.in/browser-mode/receiver",
  );

  assert.match(bookmarklet, /^javascript:/);
  assert.match(bookmarklet, /www\.yt2trascript\.in\/browser-mode\/receiver/);
  assert.match(bookmarklet, /segments/);
  assert.match(bookmarklet, /text:\s*formatRawTranscript\(segments\)/);
  assert.doesNotMatch(bookmarklet, /onrender\.com|localhost:10000|\/api\/transcript|NEXT_PUBLIC_BACKEND_URL/);
  assert.doesNotThrow(() => new Function(bookmarklet.slice("javascript:".length)));
});

test("raw transcript TXT and SRT files are produced without AI", () => {
  const payload = {
    video: { videoId: "M7lc1UVf-VE", title: "Demo video" },
    segments: [{ start: 1, duration: 2, text: "Local raw caption" }],
  };

  assert.equal(formatRawTranscript(payload.segments), "[00:01] Local raw caption");
  assert.deepEqual(createTranscriptFile(payload, "txt"), {
    filename: "Demo video.txt",
    content: "[00:01] Local raw caption",
    mimeType: "text/plain",
  });
  assert.match(createTranscriptFile(payload, "srt").content, /00:00:01,000 --> 00:00:03,000/);
});

