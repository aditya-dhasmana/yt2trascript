for (const action of ["clean", "summary", "notes"]) {
  test(`AI ${action} failure restores raw transcript and keeps downloads usable`, () => {
    const rawText = "[00:01] Raw transcript remains ready";
    const failure = createAiFailureState({
      action,
      code: action === "clean" ? "AI_UNDER_DEVELOPMENT" : "AI_UNAVAILABLE",
      rawText,
    });
    const payload = {
      video: { videoId: "M7lc1UVf-VE", title: "Failure-safe transcript" },
      segments: [{ start: 1, duration: 2, text: "Raw transcript remains ready" }],
    };

    assert.deepEqual(failure.activeOutput, { kind: "raw", text: rawText });
    assert.match(failure.message, /Raw transcript is safe and ready/);
    assert.equal(createTranscriptFile(payload, "txt").content, rawText);
    assert.match(createTranscriptFile(payload, "srt").content, /Raw transcript remains ready/);
  });
}

test("backend offline and under-development errors use product-safe copy", () => {
  assert.equal(
    getFriendlyAiFailureMessage("summary", "AI_UNAVAILABLE"),
    "Summary is temporarily unavailable. Raw transcript is safe and ready.",
  );
  assert.equal(
    getFriendlyAiFailureMessage("clean", "AI_UNDER_DEVELOPMENT"),
    "Clean transcript is under development right now. Raw transcript is safe and ready.",
  );
});

